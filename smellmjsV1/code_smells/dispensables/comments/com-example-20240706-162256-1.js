function findTriangleArea(base, height) {
  // This is the base of our triangle
  let triangleBase = base;
  // This is the height of our triangle
  let triangleHeight = height;
  // We will return the calculated area, which is (base * height) / 2 as the area of our triangle
  return (triangleBase * triangleHeight) / 2;
}

console.log(findTriangleArea(5, 4)); // This will print calculated area of the triangle