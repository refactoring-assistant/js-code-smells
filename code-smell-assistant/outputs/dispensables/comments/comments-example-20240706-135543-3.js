// Defining a function to check if the input number is Even or Odd
function checkEvenOrOdd (num) { 
  // if number is divisible by 2, it is even
  if (num % 2 == 0) { 
    // Return "Even"
    return "Even"; 
  } else {
    // Else, the number is odd
    return "Odd"; 
  } 
}

